/*
 * Operadores Aritiméticos
 */

// multiplicação *
console.log(3 * 2) //retorna: 6

// divisão /
console.log(8 / 2) // retorna: 4

// soma +
console.log(12 + 4) // retorna: 16

//subtração -
console.log(10 - 2) // retorna: 8

//resto da divisão %
let remainder
remainder = 11 % 10
console.log(remainder) // retorna: 1

//incremeto ++
let incremet = 0
incremet++ //soma de 1 em 1
console.log(incremet) // Ou => console.log(++incremet), se incremetar depois (incremet++) o valor fica 0 (zero)

// decremento --
let incremet = 0
incremet-- //subtrai de 1 em 1
console.log(incremet) // Ou => console.log(--incremet), se decrementar depois (incremet-) o valor fica 0 (zero)

// exponencial **
console.log(3 ** 2) // retorna: 9
