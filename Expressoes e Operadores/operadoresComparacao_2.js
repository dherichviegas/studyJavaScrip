/*
 * Operadores de comparação
 */

// Irá comparar valores e retornar um Boolean como respota à comparação

let one = 1
let two = 2

// === estritamente igual a (iguala o valor e o tipo do dado)
console.log(two === 1) // true
console.log(one === '1') // false

// != estritamente diferente de (difere o valor e o tipo do dado)
console.log(one !== 1) // false
console.log(one !== '1') // true
