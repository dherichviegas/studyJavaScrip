/*
  Variáveis
   - Nomes simbólicos para receber algum valor
   - atalhos de código
   - Identificadores
   - 3 palavras reservadas para criar uma variável
    * var
    * let
    * const
 
*/

var clima = 'frio'
clima = 'quente' //atualiza o valor

console.log(clima)

/**
 *  JS é uma linguaguem fracamente tipada e dinâmica
 *  - variáveis não precisam ter um tipo previamente definido
 *  - podemos mudar o contúdo da variável
 */

let clima = true
clima = '' //atualiza o valor

console.log(typeof clima) //visualizar o tipo da variável
