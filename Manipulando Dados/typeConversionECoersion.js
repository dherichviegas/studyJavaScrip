/**
 *  Type Conversion (typecasting) VS Type coersion (Coersão)
 * 
 *  Alteração de um tipo de dado para outro tipo
 
 
*/

// Type Coersion : tranformou o number 5 em strign '5' => 95
console.log('9' + 5) // resultado = 95

// Type Conversion : converteu a string '9' em number 9 => 14
console.log(Number('9') + 5) // resultado 14
