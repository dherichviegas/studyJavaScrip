/**

  Estrutura de Repetição: FOR...OF

*/

// server para string e array

//Exemplo 1:
let name = 'Viegas'

for (let char of name) {
  console.log(char)
}
/*
Retorna: 
  V
  i
  e
  g
  a
  s
*/

// Exemplo 2:
let names = ['Viegas', 'Diele', 'Ana']

for (let name of names) {
  console.log(name)
}
/*
Retorna:
  Viegas
  Diele
  Ana  
*/
